
public class Switch_test1 {

	public static void main(String[] args) {
		int month = 5;
		switch(month) {
		case 1:
			System.out.println("1월");
		case 2:
			System.out.println("2월");
		case 3:
			System.out.println("3월");
		case 4:
			System.out.println("4월");
		case 5:
			System.out.println("5월");
		}

	}

}
